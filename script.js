/* ===========================
   LANGUAGE SWITCHER
=========================== */
let currentLang = 'es';

const themeLabelMap = {
  es: {
    dark: 'Cambiar a modo noche',
    light: 'Cambiar a modo claro'
  },
  ca: {
    dark: 'Canviar a mode fosc',
    light: 'Canviar a mode clar'
  },
  en: {
    dark: 'Switch to dark mode',
    light: 'Switch to light mode'
  }
};

function applyLang(lang) {
  currentLang = lang;
  const dict = i18n[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Update <html lang>
  document.documentElement.lang = lang === 'ca' ? 'ca' : lang === 'en' ? 'en' : 'es';

  // Update active button state for ALL lang buttons (nav + mobile)
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  updateThemeToggleLabels();
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    const lang = btn.getAttribute('data-lang');
    applyLang(lang);
  });
});

/* ===========================
   THEME TOGGLE
=========================== */
const THEME_KEY = 'portfolio-theme';
const themeButtons = document.querySelectorAll('[data-theme-toggle]');

function getInitialTheme() {
  const savedTheme = localStorage.getItem(THEME_KEY);
  if (savedTheme === 'dark' || savedTheme === 'light') {
    return savedTheme;
  }

  return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
}

function updateThemeToggleLabels() {
  const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
  const labels = themeLabelMap[currentLang] || themeLabelMap.es;
  const nextLabel = isDark ? labels.light : labels.dark;

  themeButtons.forEach(btn => {
    btn.setAttribute('aria-label', nextLabel);
    btn.setAttribute('title', nextLabel);
    btn.textContent = isDark ? '☀' : '🌙';
  });
}

function applyTheme(theme) {
  document.documentElement.setAttribute('data-theme', theme);
  localStorage.setItem(THEME_KEY, theme);
  updateThemeToggleLabels();
}

function toggleTheme() {
  const currentTheme = document.documentElement.getAttribute('data-theme') || 'light';
  applyTheme(currentTheme === 'dark' ? 'light' : 'dark');
}

themeButtons.forEach(btn => {
  btn.addEventListener('click', toggleTheme);
});

applyTheme(getInitialTheme());

